  bool has_edge(const Node& a, const Node& b) const {
    // HW0: YOUR CODE HERE
    //(void) a; (void) b;   // Quiet compiler warning
    if (edgeset_size_ == 0) return false;
    Edge edge_ab = Edge(a.idx_, b.idx_);
    for(unsigned i=0; i<edgeset_size_; i++){
      Edge curr_edge = *edges_[i];
      if (curr_edge == edge_ab) return true;
    }
    return false;
  }

